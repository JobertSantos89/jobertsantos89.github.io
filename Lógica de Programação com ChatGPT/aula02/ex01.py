# Ex01. Faça um programa que peça 2 numeros ao usuário 
# (com o input) e mostre o produto deles (multiplicação).

# Entrada
numero1 = float(input('Digite o 1º numero: '))
numero2 = float(input('Digite o 2º numero: '))

# Processamento
multiplicacao = numero1 * numero2

# Saída
print(f'{numero1} x {numero2} = {multiplicacao}')