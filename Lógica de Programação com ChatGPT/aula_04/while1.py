# Estrutura de Repetição While
# Exemplo 1
contador = 0 # Variavel de Controle

while contador < 10: # Condição de CONTINUAÇÃO
    print('Olá!')
    contador = contador + 1 # Incremento

print('Fim do Programa: ')

# Exemplo 2
contador = 0 

while contador < 10: # Condição de CONTINUAÇÃO
    contador = contador + 1 # Incremento
    print(contador)

print('Fim do Programa!')